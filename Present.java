public class Present {

	private int cid;
	private int gid;
	private int slhid;

	public Present(int cid, int gid, int slhid) {
		this.cid = cid;
		this.gid = gid;
		this.slhid = slhid;
	}

	public int getCid() {
		return cid;
	}

	public int getGid() {
		return gid;
	}

	public int getSlhid() {
		return slhid;
	}
}
